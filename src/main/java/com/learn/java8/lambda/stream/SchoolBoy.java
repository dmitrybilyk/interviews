public class SchoolBoy {
    private String name;
    private String form;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public SchoolBoy(String name, String form) {
        this.name = name;
        this.form = form;
    }

    public String getForm() {

        return form;
    }

    public void setForm(String form) {
        this.form = form;
    }
}
